    public void SortDisplay(ArrayList<ShowListSchedule> list) {
        tblList.setDefaultRenderer(Object.class, new DefaultTableCellRenderer() {
            @Override
            public Component getTableCellRendererComponent(JTable table,
                    Object value, boolean isSelected, boolean hasFocus, int row, int col) {

                super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, col);

                boolean isConfirm = list.get(row).isConfirm();

                //Color a = new Color(224, 255, 252);
                if (isConfirm) {
                    setBackground(Color.WHITE);
                    setForeground(Color.BLACK);
                } else {
                    setBackground(Color.RED);
                    setForeground(Color.WHITE);
                }
                return this;
            }
        });
        int i = 1;
        model = (DefaultTableModel) tblList.getModel();
        for (int j = 0; j < list.size(); j++) {
            ShowListSchedule sch = list.get(j);
            //convert time
            String time = ConvertDate(sch.getTime());

            //add 1 doi tuong
            model.addRow(new Object[]{
                i++, sch.getDate(), time, sch.getFrom().trim(), sch.getTo().trim(), sch.getFlightNumber(),
                sch.getMakeModel(), sch.getTotalSeats(), "$" + sch.getEconomyPrice(), "$" + sch.getBusinessPrice()
            });
        }
    }

